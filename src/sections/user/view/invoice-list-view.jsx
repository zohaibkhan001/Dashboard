const TABLE_HEAD = [
  { id: 'transaction_id', label: 'Transaction ID' },
  { id: 'type', label: 'Type' },
  { id: 'amount', label: 'Amount' },
  { id: 'status', label: 'Status' },
  { id: 'payment_method', label: 'Payment Method' },
  { id: 'description', label: 'Description' },
  { id: 'createdAt', label: 'Payment Date' },
  { id: '' },
];

